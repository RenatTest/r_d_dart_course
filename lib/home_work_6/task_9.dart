void main() {
  //   Завдання 9: Цикл
  // 1) Почніть із числа 10.
  // 2) Виводьте числа, зменшуючи їх на 1, поки не досягнете 0.
  // 3) Якщо число парне — вивести “Парне число: …”, якщо непарне — “Непарне число: …”.

  int number = 10;
  do {
    number % 2 == 0
        ? print('Парне число: $number')
        : print('Непарне число: $number');
    number--;
  } while (number > 0);
}
